						<h4 class="card-title pad80">My Project Idea</h4>
						<hr class="teal-border"/>
						<h4 class="header-lv1">Overview</h4>
						<p>My project is about building a website that can take a job advertisement as an input – assuming most job ads are available online – and searches a database for identical or similar keywords. The user can also put in an industry filter so that more relevant phrases can be displayed. So, the user will be provided with whole sentences of similar job cover letters and resumes, available on the Internet and can then select what he/she would like to use for their cover letter and resume. In a more advanced version – which is likely not part of this course – these selected phrases can then be combined in a Word document. The project might even go to the extent to be able to utilise specific templates.</p>
						<h4 class="header-lv1">Motivation</h4>
						<p>In the last two years, I have written many applications for jobs. After the current Covid crisis, many people are in the same situation as me and are looking to either return to work or find something else. According to the <a class="onlight" href="https://www.abs.gov.au/statistics/labour/employment-and-unemployment/labour-force-australia/latest-release" target="_blank" alt="link to ABS unemployment statistics">ABS</a>, there are currently nearly 790,000 unemployed in Australia alone. In the US, there are currently around 9.3 million unemployed, according to the <a class="onlight" href="https://www.bls.gov/news.release/pdf/empsit.pdf" target="_blank" alt="link to BLS unemployment statistics pdf file">BLS</a>. So there is already a big market in Australia alone, not to mention the market a worldwide reach can provide. While there are many resume writing businesses, they often seem bound by certain rules and regulations and appear to be not very flexible when it comes to changing careers. Apart from that, their services are usually quite expensive. This is where my website will come to play, which is initially free and later on can be accessed via a monthly subscription.</p>
						<h4 class="header-lv1">Description</h4>
						<p>When you open up the website, you will be presented with a simple search field that will take the URL of your chosen job advertisement. In a drop-down field, just below, you can pick from a list of industries. This field will represent the usual job categories like on the seek website, for example. When the user presses the search function, a PHP script will load the job advertisement in a buffer and screen for “about”, “required”, and “role” sections and extract the keywords. These sections are often in a &lt;ul&gt; tag or a &lt;p&gt; tag. The contents of these tags will be processed and screened against a database of known and common keywords. The script will then parse through these elements and collect matching keywords. These are then used to retrieve whole sentences from the database that contain these keywords, which then will be presented to the user in a list.<br/>
								The database has the contents tagged with the different industries they relate to and then filters them accordingly. Of course, some of these result sentences can have multiple tags as they can be used in different industries. These whole sentences are then selected and displayed. The user can also define how many examples should be displayed at all in the initial search mask.<br/>
								The data for the database will be collected with scripts that search the Internet for sample resumes and cover letters. It will also search within images, utilising OCR to extract text from those and store it in the database. One big problem I have not worked out yet is duplicates. Indeed, when scraping the Internet, the script will come up with a lot of similar or even identical results. There should be tools that compare word by word sentences to eliminate identical duplicates. However, with similar sentences, it will be more difficult. I am confident that someone will have an idea of how to do that.<br/>
								The script also needs to screen for specific job titles within the scraped data to tag the results automatically. As we compare with a specific list of industries, that should not be too much of a problem as everything that follows will be tagged according to the matching industry.<br/>
								The results will initially be just displayed in a list on the screen so that the user can copy and paste the sentences they like. In a more sophisticated, later version, I am planning to incorporate functionality to export the selected sentences to a word document or even provide the option to store a template – with the data that never changes – and have a complete word document exported.</p>
						<h4 class="header-lv1">Tools and Technologies:</h4>
						<p>To realise this project, all it needs is a computer and a hosting web service. However, the software for this project requires tools to build webpages and handle PHP code and a MySQL database installed on the webserver. Nearly every program from TextEdit to Notepad, over to Atom and Dreamweaver can fulfil this need. For the scraping function of web content, Python seems to be a good option as it can return complete websites with the BeautifulSoup package and has the functionality to handle the output. PHP also has the functionality to work with strings and regular expressions, which should help in further processing of the user input. So, Python to fill the database and PHP to access it.</p>
						<h4 class="header-lv1">Skills Required</h4>
						<ul class="on-light">
								<li>Advanced programming skills in Python and PHP</li>
								<li>Database management skills, especially in MySQL</li>
								<li>Web design skills</li>
								<li>Software to be written:</li>
								<ul class="on-light">
										<li>Program to scrape the web for resume and CV samples</li>
										<li>Program to analyse these samples and feed them into the database</li>
										<li>Program to receive user input and compare this to database entries</li>
										<li>HTML script for display of website, plus CSS and JS for functionality</li>
								</ul>
						</ul>
						<h4 class="header-lv1">Outcome</h4>
						<p>If this website can be realised, it will help thousands of job seekers to write cover letters and resumes more easily. Even if they do not want to utilise the results as is, they still will give them ideas for what to write, and the hours of staring at the wall, thinking about what to put in the cover letter or resume will be over. As the average job-seeker writes dozens to hundreds of resumes, this will significantly improve the speed with which they can progress.</p>
